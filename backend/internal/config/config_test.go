func TestValidateRejectsMissingSecret(t *testing.T) {
	t.Parallel()

	cfg := &Config{
		DatabaseURL: "postgres://example",
		SecretKey:   "",
		JWTSecret:   "",
	}

	if err := cfg.validate(); err == nil {
		t.Fatal("expected missing secrets to be rejected")
	}
}

